                <div class="as-team-detail">
                  <p>Paul P. Harris, an attorney, wanted to create a professional group with the same friendly spirit he felt in the small towns of his youth. On 23 February 1905,
                   Harris, Gustavus Loehr, Silvester Schiele, and Hiram Shorey gathered at Loehr’s office in Room 711 of the Unity Building in downtown Chicago. This was the first
                   Rotary club meeting. They decided to call the new club “Rotary” after the practice of rotating meeting locations.</p>
                   
                  <p>Within five years clubs had formed across the country, from San Francisco to New York.
        		  <br />
        		  In August 1910, Rotarians held their first convention in Chicago. The 16 clubs that existed at that time united to form the National Association of Rotary Clubs.
                  </p>
                  
                  <p>
                  In 1912, the name changed to International Association of Rotary Clubs to reflect the addition of clubs in other countries. The name Rotary International was
                  adopted in 1922.<br />By July 1925, Rotary had grown to more than 2,000 clubs and an estimated 108,000 members  on six continents.
                  <br />
                  Rotary’s reputation attracted presidents, prime ministers, and a host of other luminaries to its ranks — among them author Thomas Mann, diplomat Carlos P. Romulo,
                  and composer Jean Sibelius.<br />
                  As Rotary grew, members pooled their resources and used their talents to serve their communities. The organization's dedication to this ideal is best expressed 
                  in its motto: Service Above Self.<br />
                  There are 1.2 million Rotarians in 34,000 clubs doing good all over the world. This network of clubs makes up Rotary International.
                  <br />As a club member you help elect your own leaders each year, and your president works with your president-elect, officers, board members, 
                  and committee chairs to manage the club. Your club pays dues to RI, and in return RI provides resources, training, and programs to help your club run effectively.
                  <br />Your club and others in your geographic area are part of a district, led by your district governor. Districts help clubs connect to each other and access
                  Rotary resources. There are around 530 districts, and these are organized into 34 zones. Each zone has about the same number of Rotarians.
                  <br />Rotary clubs sponsor other service clubs and groups that broaden our reach :
                  <ul style="text-align:left; margin:7px 0 0 0; padding:0 0 0 20px; list-style:disc; font-family:Verdana, Arial, Helvetica, sans-serif">
