                     <br>
                      By devoting approximately 1 hour/day for 5 days a week, a learner can complete a course in a period of 2 months and thus be more skilled to compete for jobs in the service sector. The courses in audio-visual format promote self-directed learning with easy to follow instructions and by presenting challenges and case studies for the learner to work on and learn through. At the end of the course, post an online evaluation, the learners will receive an e-certificate.    
                        <br>
                        <br>
                        <strong>What is the Role of Rotary Clubs? </strong>
                       <br>
                       <br>
                       <ul>
                        
                        <li>Rotary Clubs will have to identify learners- underprivileged persons, 14+ years of age, who are seeking to acquire vocational skills for their career development. They could be anyone- from students of Govt. Schools and Colleges, head loaders, taxi drivers, house maids, tea vendors and so on- anyone who has the interest to apply for decently paid respectable jobs in the service sector but lacks the skills required.</li>
                        <li>Rotary Clubs will support the learner through payment of the course fee – Rs.1000/learner and registering the learner for any of the courses listed above. Please contact program@rotaryteach.org for further details.</li>
                        <li>Rotary Clubs will identify the venue based on the model it chooses:
                          <ul>
                            <li style="list-style:none;">
                            a.	Online model: Courses maybe offered through Computer Labs (with internet connection) of Schools/Colleges, Cyber Cafes etc. </li>
                            <li style="list-style:none;">
                            b.	Off-line model: The content is loaded onto a local server (a computer) and thus available to other computers connected by a LAN to it. The local
                                server needs to simply connect to the internet once in two days.</li>
                          </ul>
						</li>
                        <li>Rotary Clubs will mentor their learners to complete the courses and help them to chart out their career paths.</li>
