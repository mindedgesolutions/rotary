                   The Rotary India Literacy Mission is the body of all Rotarians of India united by the Goal of Total Literacy and Quality Education.
				     At the Rotary South Asia Conference on Development and Co-operation held in Colombo, Sri Lanka in September 2011, Rotarians from India, Maldives, Nepal,
                     Pakistan and Sri Lanka came together and decided to work for development of the region in the areas of education, health and hygiene, trade and commerce, 
                     youth and sports, empowerment of women, etc.
                    <br /><br />
                    To create a formal legal entity that could plan, fund and implement various initiatives in the chosen fields, the Rotary South Asia Society for Development 
                    and Co-operation (RSAS) was registered in Mumbai as a society under the Societies Registration Act, 1861 in February 2014.
                    <br /><br />
                    In between, the Rotarians of the region met at Kathmandu, Nepal in March 2011 and pledged to work together to bring Total Literacy in South Asia. At the South 
                    Asia Literacy Summit held in New Delhi in December, 2013, the Rotarians of India resolved to form the Rotary India Literacy Mission (RILM) and implement its
                    program “T-E-A-C-H” to achieve the literacy goals.
                    <br /><br />
                    Other countries in the region like Bangladesh, Nepal and Pakistan have also taken steps to form similar national Literacy Missions and implement literacy 
                    programs with the same objectives.
                    <br /><br />
                    This Society subsequently adopted the Rotary India Literacy Mission, its Committee Structure and the T-E-A-C-H program in May 2014.
