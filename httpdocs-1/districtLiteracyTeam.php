		<div id="panel3">
			<div class="headingH3"><h3>Role & Responsibilities </h3></div>
			<p>The Rotary Club is the organization that is at the centre of all activities relating to implementation of program in the T-E-A-C-H program and the Club President/President Elect (CP/CPE) is the key figure who, in collaboration with the Club Literacy Committee Chair (CLCC) and Members (CLCMs) is expected to plan, select, execute, supervise and report on the program specific activities that they choose to take up. </p>
			<p>Keeping this in view, the role and responsibilities of the CP/CPE are defined as follows :</p>
			<ul style="list-style:lower-roman;">
				<li>To appoint the CLCC and Club Literacy Committee Members well before the Level II training program (expected to be held by 31st May each year) so that all of them (along with the CP/CPE) can participate fully in that training program</li>
				<li>To attend the Level II training program organized by the District Governor/Governor Elect (DG/DGE) and the District Literacy Committee Chair (DLCC), along with the CLCC and CLCMs, and fully understand each program/component of the T-E-A-CH program as well as its funding and reporting aspects</li>
				<li>To motivate members of the Club to attend in large numbers the Level III training program (District Literacy Seminar) as and when it is held by the District leader-ship (expected during 15th May - 31st August each year)</li>
				<li>To study the www.rotaryteach.org website carefully and download all information, forms, etc., that are necessary for implementation of the selected program.</li>
				<li>To familiarize himself and all members of the Club Literacy Committee with the “how to” guidelines of each activity/program of T-E-A-C-H, grant application forms for various programs as well as the quarterly progress report formats</li>
				<li>Based on the training and information so gathered, to decide, well before the beginning of the Rotary Year (RY), the T-E-A-C-H activities/programs that the Club will like to take up during the year and plan for timely implementation of each so that they can be all completed in all respects before the end of the RY</li>
				<li>To tie up arrangements for the necessary mobilization and training of volunteers and Club members, funds and other inputs for implementation all selected activi-ties and sequence them appropriately</li>
				<li>To explore and finalize partnerships with other organizations and non-governmental organizations (NGOs) where such partnership will be essential for imple-mentation of any selected set of activities</li>
				<li>To tie up funding, including grants from Rotary India Literacy Mission (RILM) for each selected activity</li>
				<li>To tie up funding, including grants from Rotary India Literacy Mission (RILM) for each selected activity
					<ul>
						<li>get the selected activities executed properly</li>
						<li>send quarterly progress reports to the DLCC</li>
						<li>upload information on the website (wherever so indicated)</li>
						<li>complete the programs</li>
						<li>finalize accounts</li>
						<li>get necessary certification of accounts done</li>
						<li>send program completion reports to DLCC and also upload them on the website</li>
					</ul>
				</li>
			</ul>
