			<div class="headingH3"><h3>Role & Responsibilities </h3></div>
			<p>The District Governor (DG) is like the Conductor of a symphony orchestra, the kingpin of all activities that the Rotary Clubs in her/his District take up in a Rotary Year. The Rotary Clubs in a District execute various programs under the guidance and leadership of the DG. </p>
			<p>In view of this multifarious role of a DG, only the most essential are listed below as the role and responsibilities of the DG in respect of T-E-A-C-H :</p>
			<ul style="list-style:lower-roman;">
				<li>To attend the Orientation program conducted by the Executive Committee of Rotary India Literacy Mission (RILM) each year</li>
				<li>To appoint (or continue the appointment of) the District Literacy Team consisting of the District Literacy Committee Chair (DLCC) and Chairs & Members of Committees for
					<ul>
						<li>Teacher Support Committee</li>
						<li>E-Learning Committee</li>
						<li>Adult Literacy Committee</li>
						<li>Child Development Committee</li>
						<li>Happy Schools Committee</li>
						<li>Volunteer Management Committee</li>
						<li>Fundraising Committee</li>
						<li>Communications Officer, before the Orientation program</li>
					</ul>
				</li>
				<li>To ensure that the Level I training program for the District is organized in consultation with the Zonal Literacy Coordinator (ZLC) before or on the due date (30th April each year) and all mandated participants actually attend the training</li>
				<li>To ensure that the Level II training program is organized by the DLCC before or on the due date (31st May each year) and attended by the Club Presidents Elect and Club Literacy Committee Chairs of at least 60% of the Clubs in the District</li>
				<li>To ensure that the Level III training program (District Literacy Seminar) is organized by the DLCC during the due period (15th May – 31st August each year)</li>
				<li>To raise per capita contribution from each Rotary Club member in the District at the agreed rate and credit the amount by 30th September each year to the RILM account</li>
				<li>To assist the National Fundraising Committee for Retail Donations in collecting funds for T-E-A-C-H. 40% of these collections can be retained by the District for T-E-A-C-H implementation</li>
				<li>Set goals and reconfirm goals through discussion with each Club</li>
				<li>Regularly motivate Clubs to achieve their goals</li>
				<li>Create awareness of and publicize the RILM programs and news from time to time through Governors Monthly Letter (GML) etc.</li>
				<li>Conduct sessions on RILM and T-E-A-C-H at Assistant Governors Trainings (AGTs), Presidents Elect Trainings (PETs), Secretary Elects Trainings (SETs), District Assembly, District Conference, etc.</li>
				<li>Respond to information requests of RILM Office & ZLC on a priority basis</li>
			</ul>
			<p>In addition to the above, the DG is expected to ensure all activities as enumerated for DLCC (for details read below).</p>
		</div>
