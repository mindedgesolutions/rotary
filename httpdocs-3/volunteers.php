                  <h2 style="text-align:center"><strong>Volunteer for a more Literate India!</strong></h2><br>
<br>

                  After the success of the “End Polio Program”, Rotary in India (Rotary India Literacy Mission) has adopted the T-E-A-C-H (<a href="teacher_support.php" style="color:#069;">Teacher Support,</a> <a href="elearning.php" style="color:#069;">E-Learning,</a> <a href="adult_literacy.php" style="color:#069;">Adult Literacy,</a> <a href="child_development.php" style="color:#069;">Child Development</a> and <a href="happy_schools.php" style="color:#069;">Happy School</a>) program to take large strides towards Total Literacy and Quality Education for India.<br>
<br>
As a volunteer we welcome you to use your talents, skills and experiences to aid RILM in its crusade of making India totally literate.


<br>
<br>
You can play a part in supporting teachers in improving the educational experience of a child, assisting a non-literate adult on the path to functional literacy as well as bringing extra-curricular activities to the fold of bridge classes attended by out-of-school children.<br>
