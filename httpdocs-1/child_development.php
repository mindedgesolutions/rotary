                  	<li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Identify children<font color="#FF0000">*</font> between 7 to 14 years, never enrolled/ dropped out/ laggard in studies from vulnerable communities.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Bring these children to intermediary centre to provide necessary supplemental/ non formal education to shore up their learning abilities. </li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Non-governmental, non-profit organisations working in the field of child education are engaged in identifying and mainstreaming these children after &nbsp;&nbsp;&nbsp;&nbsp;undersigning a MoU. This program is implemented through organisations registered under either societies or trust act. </li>
                  <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Counsel and encourage the parents to send their children to regular formal schools. </li>  
                  <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Mobilise community on “Right to Free and compulsory Education Act 2009” </li>
                  <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Evaluate learning ability, prepare the children and enroll them to local Government or Government-aided schools.</li>
                  <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Monitor the attendance of the child in schools for 3 to 6 months.</li>
                  <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Every sponsored child’s progress & development report will be made available on the website and they will be tracked till 3 months after they resume formal &nbsp;&nbsp;&nbsp;&nbsp;school.</li>
