                  <div class="as-strip-title"> <h3><strong>THE TEACHER SUPPORT PROGRAM</strong></h3> </div> 
                  <p>Our aim is to improve the professional abilities and performance of teachers in selected primary/elementary schools through better training and use of modern 
                  teaching aids, so as to enhance the learning outcomes of students.
                   <br /><br />
                  <div class="as-strip-title"> <h3><strong>ACTIVITIES UNDER THE PROGRAM</strong></h3> </div>  
                  <h3><strong><p style="font-size:25px;">A. Training/ Re Training of Teachers :</p></strong></h3> 
                  <p>Teacher training is the main focus of the entire Teacher Support vertical. The role of teachers in shaping the minds of the youth is undeniable. Teachers from government schools undergo pre service as well as in service training during their career. However, apart from this, regular workshops, seminars and training should be encouraged to enhance the capabilities of a teacher. There are various institutions that specialize in training teachers. Rotary India Literacy Mission’s Teacher Training program focuses on providing such facility to the government schools and government aided schools across India by collaborating with institutions that specialize in this. </p>
                  <p><strong>Program Implementation :</strong></p> 
                   <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Identify primary or elementary government/government aided schools requesting them to participate.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Arrange for interactive workshops may focus on effective innovative teaching methodology such as activity-based participative learning,
                     e-learning, classroom management etc. </li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Encourage teachers to discuss the problems they face in teaching and collectively arrive at innovative solutions to improve the
                    teaching-learning experience.</li>
                  </ul>
                 
                  <br>
                  <div id="nba">
                  <h3><strong><p style="font-size:25px;">B. Nation Builder Awards :</p></strong></h3>
                  <p>One major component of the Teacher Support program is recognising outstanding teachers through the “Nation Builder Award”. Recognizing outstanding teachers
                   instills them with renewed confidence and encourages them to perform better. It also gives an idea about the less effective teachers in the surveyed schools, who
                   are later helped with training sessions and workshops to enhance their performance. 
                  </p>
                  <br> 
                  <p><strong>Program Implementation</strong></p>
                  <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                  	<li style="margin-bottom:15px; line-height:18px;color:#000000;">Understand the evaluation process by downloading the three forms from the resources section in
                     the Member Zone of the website.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Identify a government school and seek consent from School Authority to conduct the evaluation. 
                    </li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">The Principal/ Head Teacher and 10 students from each school to participate in the evaluation 
                    process.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Conduct Evaluation with NB 1.1 and NB 2.1 forms. </li>
                   <li style="margin-bottom:15px; line-height:18px;color:#000000;">The evaluation process should be anonymous. </li>
                   <li style="margin-bottom:15px; line-height:18px;color:#000000;">Compilation of marks in NB 3.1 / Online calculation.</li>
                   <li style="margin-bottom:15px; line-height:18px;color:#000000;">Felicitating the outstanding teacher with certificates on Teachers' Day, 5th September.</li>
                   <li style="margin-bottom:15px; line-height:18px;color:#000000;">Upload project details on RILM website.</li>
                   <li style="margin-bottom:15px; line-height:18px;color:#000000;">Involve the less effective teachers in training workshops to enhance their competency. 
                   </li>
                  </ul>
				  </div>
                  <br>
                  <h3><strong><p style="font-size:25px;">C. Supplemental Teaching in Schools :</p></strong></h3>
                  <p>Often in Government/Government-Aided schools there is a severe dearth of teachers,as a result of which one teacher has to teach a number of subjects in which
                   s/he is not specialized. This not only becomes hectic for the teacher but also the quality of education is compromised. The best possible solution to this is 
                   providing supplemental education in schools through volunteers.
                  </p>
                  <br> 
                  <p><strong>Program Implementation</strong></p>
                  <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                  	<li style="margin-bottom:15px; line-height:18px;color:#000000;">Select a Government/Government-aided primary or elementary school and speak to the Prinicipal to
                     determine whether the school requires volunteer teachers to give &nbsp;&nbsp;extra classes to students of various classes, to improve their academic
                     performance.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Volunteer teacher should have a minimum educational qualification commensurate to the class s/he
                     is teaching and be ready to provide the services for minimum 15 days.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">
                    Once volunteers are finalized, the Rotary Club may introduce him/her to the Head Teacher</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">
                    Ensure that the volunteer teacher receives some orientation about the requirements of the students from the teachers and also from the School Management
                    Committee.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">
                    The volunteer teacher may be encouraged to submit a report periodiocally to showcase his/her teaching experience.</li>
                  </ul>
                  <br>
                  <h3><strong><p style="font-size:25px;">D. Strengthening of District Institute of Education and Training (DIET) :</p></strong></h3>
                  <p>Before the formation of District Institute of Education and Training (DIET), teacher training in the area of elementary education was looked after by NCERT & 
                  SCERT at the National and State level. The establishment of DIET based on the recommendations of the National Education Policy, 1986, came into being to improve
                  the role of teacher and thus influence the quality of education at the district level.
                  </p>
                  <br>
				  <p>The chief purpose of this is to improve the learning achievements of students by : </p>

                  <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Pre-service training i.e. to prepare teachers for the school system.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">In-service training i.e. to improve the quality of existing school teachers.</li>
                  </ul>
                  <br> 
                  <p><strong>Program Implementation</strong></p>
                  <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                  	<li style="margin-bottom:15px; line-height:18px;color:#000000;">Visit a DIET after contacting the Principal with prior permission taken from the State Council of Educational Research and Training and knowledge of 
                    requisites on the areas &nbsp;&nbsp;&nbsp;&nbsp;of improvement.</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Detailed study of the DIET to be made with guidance from the T6 form designed for DIET survey (available in Resources under Teacher Support Vertical).</li>
                    <li style="margin-bottom:15px; line-height:18px;color:#000000;">Assist the DIET in areas like improving the library or other infrastructural improvement.</li>
                  </ul>
