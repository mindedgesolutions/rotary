                  <h2 style="">Sending 100,000 children back to school in India in 2016 - 17.</h2>
                  <br>
                  <br>
                  <p>India has 6 million out of school children between the age group of 7-14 year, of which 44.5% who never went to school, 36.97% who are drop outs and 18.53% who 
                  enrolled but never attended school
                   <br>
                   <br>
                   <h3><strong>THE CHILD DEVELOPMENT PROGRAM</strong></h3>
                   Through this program we are to sending children back to school. Children from vulnerable communities, who have never been enrolled to any school or have been
                   dropped out of school, are being sent back to school after required brushing up. The objective is to facilitate these children’s access to mainstream state funded
                   primary/ elementary schools.<br><br> 
                   <div class="as-strip-title"> <h3><strong>ACTIVITIES UNDER THE PROGRAM</strong></h3> </div>  
                  <h3><strong>Asha Kiran… a ray of hope :</strong></h3> 
                  <p>This is the name given to the Child Development initiative to send 100,000 out of school children back to school. This program has been undertaken to fulfil the
                   goals of Child Development vertical of RILM.</p>
                  <br>
