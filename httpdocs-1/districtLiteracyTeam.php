				<li>To attend the Literacy Orientation Program and Literacy Summit each year, thoroughly understand the T-E-A-C-H program as well as the related Central, State and local government policies, programs and initiatives and also ensure maxi-mum participation of delegates from the District in the Literacy Summit</li>
				<li>To ensure formation of all District level committees with the approval of the DGE well before the scheduled date of Level I Training Program</li>
				<li>To assist the Zonal Literacy Coordinator (ZLC), District Governor (DG)/District Governor Elect (DGE) in organizing and conducting Level I Training Program by/ before the due date and ensure participation of all mandated participants</li>
				<li>To ensure appointment of Club Literacy Committee Chairs (CLCCs) and formation of all Club level committees by each Club President Elect well before the scheduled date of Level II Training Program</li>
				<li>To assist the District Governor (DG)/District Governor Elect (DGE) in organizing and conducting Level II Training Program by/before the due date and ensure participation of all mandated participants</li>
				<li>To ensure filling up of Club goal forms during Level II Training Program and com-pile District goals based thereon and upload the latter on the website</li>
				<li>To assist the District Governor (DG)/District Governor Elect (DGE) in organizing and conducting Level III Training Program during the time slot prescribed for this purpose and ensure participation of all mandated participants</li>
				<li>To ensure participation of Inner Wheel Clubs in the District, Rotaractors and Rotary Community Corps (RCC) members in training at the District/Club level</li>
				<li>To encourage and motivate each Club to :
					<ul>
						<li>upload grant applications/programs before and after completion</li>
						<li>register & train volunteers according to needs of Club’s planned programs,</li>
						<li>organize events to publicize important achievements under T-E-A-C-H,</li>
						<li>mobilize funds for program implementation</li>
						<li>visit and study the website www.rotaryteach.org regularly</li>
					</ul>
				</li>
				<li>To trouble-shoot for Clubs and District program committees with all concerned, including government officials at the revenue District and state levels</li>
				<li>To take all steps, including trouble-shooting with other stakeholders and government officials, in consultation/collaboration with ZLC, DG/DGE, Chairs of District level program committee, Club Presidents and CLCCs so as to ensure that the District Goals are achieved</li>
				<li>To prepare and furnish annual, quarterly and other reports as required from time to time</li>
			</ul>
