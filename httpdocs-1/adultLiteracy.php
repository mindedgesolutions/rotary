				<p>This project seeks to educate adult non-literates in urban and rural areas, provide opportunities for continuing education to such neo-literates and impart skill development training to adult neo-literates leveraging available government assistance.</p>
				</br>
				<p> The schemes included in this project are:</p>
				<ul style="list-style-type:lower-roman">
					<li>Selection of adult (15+ years) non-literate men and women
						<ul style="list-style:disc">
							<li>in urban slums/bastis and rural areas proximate to Rotary Clubs</li>
							<li>with emphasis on working women, adolescent girls mothers/home-makers, and</li>
                            <li>imparting basic literacy to such adults
							<ul style="list-style: lower-alpha;">
								<li>through selected qualified volunteers and</li>
								<li>using available primers, etc., and, wherever feasible, e-learning modules</li>
							</ul>
							</li>
						</ul>
					</li>
					<li>Facilitating active participation of Rotarians, their spouses, Inner Wheel members, Rotaractors, Interactors and Rotary Community Corps (RCC) members to register and work as volunteers for above-mentioned activities.</li>
					<li>Encouraging individual Rotarians and their family members to also ensure imparting literacy to/upgrading literacy status of all members of their office and domestic staff.</li>
					<li>Providing opportunities to the neo-literates for Continuing Education (CE) by creating community libraries, organizing quiz contests, etc.</li>
					<li>Arranging for vocational training for skill development or skill up-gradation to
					  <ul style="text-align:justify;list-style: disc; ">
                            <li>unemployed adult neo-literates, and</li>
                            <li>literate adults employed in low-skill jobs</li>
                        </ul>
					</li>
				</ul>
				</br>
				by availing of the schemes of the National Skill Development Corporation and of similar organizations of the central/state governments so as to augment their income and thus motivate them to seek literacy and vocational/skill development training.
				
