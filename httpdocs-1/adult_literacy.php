                  <h2 style=""><strong>Educating 100,000 adult non-literates across the country in India in 2016 - 17.</strong></h2>
                  
                  
                  <p>According to a UNESCO Report of 2013-14 India has by far the largest population of illiterate adults, amounting to 37 % of the global total.
                   <br>
                   <br>
                   <h3><strong>THE ADULT LITERACY PROGRAM</strong></h3>
                  
                   <p>Our aim is to indentify adult (15+years) non-literate person in both rural and urban areas, and impart basic literacy to them through.</p>
                   <aside class="widget widget_info col-md-12">
             		 <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                      	<li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Qualified & trained volunteers</li>
                        <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Using available primers and where ever feasible E-learning modules</li>
                        <li style="margin-bottom:15px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;Adult Literacy Centres</li>
                     </ul>
                   </aside>
                   <p>Provide opportunities for continuing education to such neo-literates and impart skill development training to them leveraging available
                    government assistance.</p>


<br>

				   <h3><strong>ACTIVITIES UNDER THE PROGRAM</strong></h3>
                   <br>
                  <h3><strong>1. DIKSHA</strong></h3> 
                  
                  In this approach student-volunteers from schools/colleges are engaged in providing functional literacy classes to non-literate adults (15+ age) over a period of 45-60 days. The learners are advised to appear for the NIOS examination at the end of the project, which takes place in March and August every year. On clearing the examination the learners are certified as functionally literate.
                  
                  <!--<font color="#FF0000">*</font> The Government of India organizes a nationwide assessment test/ examination for adult learners under Saakshar Bharat, every year in March and August.--> 
                  <br>
                  <br>The examinations are conducted through:
                  <aside class="widget widget_info col-md-12">
                  <ul style="text-align:left; margin:5px 0 0 0; padding:0 0 0 25px;">
                  	<li style="margin-bottom:5px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;National Literacy Mission Authority (NLMA) in collaboration with National Institute of Open Schooling (NIOS) - India</li>
                    <li style="margin-bottom:5px; line-height:18px;color:#000000;">&diams;&nbsp;&nbsp;State Literacy Mission Authority (SLMA) in collaboration with State Resource Center (SRC) - State.</li>                    
                   </ul>
                   <br>
                  </aside> 
                  
				  <br>
                  <h3><strong>2. SWABHIMAAN – Dignity Through Education :</strong></h3>Swabhimaan envisions setting up of Adult Literacy 
				  Centres (ALC) in both rural and urban areas in a centralized location to impart functional literacy to non-literate
				  adults (15+ years). Maximum 30 learners can be enrolled per batch and multiple batches can run in one ALC. The main 
				  resource for this intervention is the teacher, who has the sole responsibility of coordinating the project, tracking 
				  and reporting the progress of the learners. It can directly be implemented by Clubs or by NGOs identified by Clubs 
				  and/or RILMO.

                  
