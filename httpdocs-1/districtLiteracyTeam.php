			<p>The Rotary Club is the organization that is at the centre of all activities relating to implementation of program in the T-E-A-C-H program and the Club President/President Elect (CP/CPE) is the key figure who, in collaboration with the Club Literacy Committee Chair (CLCC) and Members (CLCMs) is expected to plan, select, execute, supervise and report on the program specific activities that they choose to take up. </p>
			<p>Keeping this in view, the role and responsibilities of the CP/CPE are defined as follows :</p>
			<ul style="list-style:lower-roman;">
				<li>To assist the Club President (CP)/President Elect (CPE) with selection of members of the Club Literacy Committee before the due date of the Level II training program/li>
				<li>To attend the Level II training program organized by the DG/DGE & District Literacy Committee Chair (DLCC)</li>
				<li>To assist CP/CPE and DLCC in mobilizing/motivating members of the Rotary Club to participate in the Level III training program (District Literacy Seminar)</li>
				<li>To decide, in consultation with the Club Literacy Committee members and with the approval of the CP & CPE, the programs under T-E-A-C-H to be taken up by the Club, well before the beginning of the Rotary Year</li>
				<li>To study the website www.rotaryteach.org and cull out all relevant information and various forms necessary for implementation of the selected programs</li>
				<li>To fill in and upload, in consultation with the Primary Contact (if not himself/her-self the Primary Contact nominated by the CP/CPE) the relevant grant application form/s for the activities/programs for which the Club wishes to avail of Rotary India Literacy Mission (RILM) grant</li>
				<li>To follow up with the DLCC for authorization of the grant application forms uploaded by the Club till each application is approved</li>
				<li>To plan and execute the selected activities/programs after tying up funds that will have to be raised by the Club for this purpose</li>
				<li>To oversee the execution of various programs taken up by the Club in association with other members of the Club Literacy Committee and ensure periodical reporting using the prescribed form/s</li>
				<li>To ensure proper accounting of all funds spent on selected programs, certification of accounts and works as required under the RILM guidelines and claiming of grants due from RILM</li>
				<li>To resolve issues that may arise in course of implementation of programs with the advice of the CP, DLCC, etc.</li>
			</ul>
