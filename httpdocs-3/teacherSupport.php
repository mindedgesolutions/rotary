                  <h2 style="">Training and recognising 5,000 outstanding teachers of primary / elementary schools across India during <br>2016 - 17.</h2>
                  <br>
                  <br>
                  <!--<h3>There are more than 7 lakh untrained teachers in our country and the requirement is of 7 lakh more.</h3>
